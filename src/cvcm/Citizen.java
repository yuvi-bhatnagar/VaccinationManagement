    private void phnjTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_phnjTextField1KeyPressed
        // TODO add your handling code here:
      String ph_num=phnjTextField1.getText();
      int length= ph_num.length();
        char c=evt.getKeyChar();
        errjLabel10.setText("");
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
        
        if(length<10)
                phnjTextField1.setEditable(true);
        
        else
        {
            phnjTextField1.setEditable(false);
            errjLabel10.setText("check your Number");
        }
        }
        else
        {
        
         if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getExtendedKeyCode()==KeyEvent.VK_DELETE||evt.getExtendedKeyCode()==KeyEvent.VK_ENTER) {      
             phnjTextField1.setEditable(true);
             
             }
        else
         {
             phnjTextField1.setEditable(false);
            
           errjLabel10.setText("Only Input Numeric Values");
        }
        }
    }//GEN-LAST:event_phnjTextField1KeyPressed

