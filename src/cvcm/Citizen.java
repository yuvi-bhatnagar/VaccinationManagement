    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(namejTextField5.getText().equals(""))
            errjLabel10.setText("Enter Name");
        else
            if(phnjTextField1.getText().equals(""))
                errjLabel10.setText("Enter Phone Number");
        else
            {
                cobj.connect();
                try
                {
                    pst=cobj.con.prepareStatement("select* from tbCitizen where Name=? and Phone=?");
                    pst.setString(1, namejTextField5.getText());
                    pst.setString(2, phnjTextField1.getText());
                    rst=pst.executeQuery();
                    if(rst.next()==true)
                    {
                        errjLabel10.setText("Record Found");
                        cid=rst.getLong(1);
                        System.out.println(cid);
                        namejTextField5.setText(rst.getString(2).toUpperCase());
                        fnamejTextField6.setText(rst.getString(3).toUpperCase());
                        adjTextField7.setText(rst.getString(4));
                        agejTextField8.setText(rst.getString(5));
                        if(rst.getString(6).equals("male"))
                            jRadioButton1.setSelected(true);
                        else
                            if(rst.getString(6).equals("female"))
                                jRadioButton3.setSelected(true);
                        else
                                if(rst.getString(6).equals("Others"))
                                    jRadioButton2.setSelected(true);
                        phnjTextField1.setText(rst.getString(7));
                        emjTextField2.setText(rst.getString(8));
                        dorjTextField3.setText(rst.getString(9));
                        adhrjTextField4.setText(rst.getString(10));
                    }
                    else
                    {
                        errjLabel10.setText("Record does not Exists");
                    
                        fnamejTextField6.setText("");
                        adjTextField7.setText("");
                        agejTextField8.setText("");
                        emjTextField2.setText("");
                        dorjTextField3.setText("");
                        adhrjTextField4.setText("");
                    }
                }
                catch(Exception ex)
                {
                    System.out.println("error in search"+ex);
                }
                cobj.disconnect();
            }
    }//GEN-LAST:event_jButton3ActionPerformed

