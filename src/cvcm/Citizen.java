    private void namejTextField5KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_namejTextField5KeyPressed
        // TODO add your handling code here:
        int key=evt.getKeyCode();
        if(key==10)
            fnamejTextField6.requestFocus();
         String name=namejTextField5.getText();
      
        char c=evt.getKeyChar();
        errjLabel10.setText("");
        if(evt.getKeyChar()>='A' && evt.getKeyChar()<='Z')
        {
            namejTextField5.setEditable(true);
        }
        else
       if(evt.getKeyChar()>='a' && evt.getKeyChar()<='z')
        {
            namejTextField5.setEditable(true);
            
        }
        
        else
            if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9')
            {
                namejTextField5.setEditable(false);
                
                errjLabel10.setText("Check your Name");
            }
        else
                if(evt.getExtendedKeyCode()==KeyEvent.VK_BACK_SPACE||evt.getExtendedKeyCode()==KeyEvent.VK_DELETE||
                        evt.getExtendedKeyCode()==KeyEvent.VK_ENTER)
                {
                    namejTextField5.setEditable(true);
                }
       
        
                
    }//GEN-LAST:event_namejTextField5KeyPressed

