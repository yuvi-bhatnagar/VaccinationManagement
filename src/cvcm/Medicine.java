    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(mnamejTextField1.getText().equals(""))
            errjLabel5.setText("Enter Medicine Name");
        else
            if(cnamejTextField5.getText().equals(""))
            errjLabel5.setText("Enter Company Name");
        else
                
                        {
                            cobj.connect();
                            try
                            {
                              pst=cobj.con.prepareStatement("select * from tbMedicine where MedicineName=? and CompanyName=?");
                              
                              pst.setString(1, mnamejTextField1.getText());
                              pst.setString(2, cnamejTextField5.getText());
                              rst=pst.executeQuery();
                             
                              if(rst.next())
                              {
                                      errjLabel5.setText("record found");
                                      mid=rst.getLong(1);
                                      mnamejTextField1.setText(rst.getString(2).toUpperCase());
                                      cnamejTextField5.setText(rst.getString(3).toUpperCase());
                                      dijTextField2.setText(rst.getString(4));
                                      dejTextField3.setText(rst.getString(5));
                                      qtyjTextField4.setText(rst.getString(6));
                              }
                              else
                              {
                                  errjLabel5.setText("record does not exists");
                              }
                            }
                            catch(Exception ex)
                            {
                                System.out.println("error in search"+ex);   
                            }
                            cobj.disconnect();
                        }
    }//GEN-LAST:event_jButton4ActionPerformed

