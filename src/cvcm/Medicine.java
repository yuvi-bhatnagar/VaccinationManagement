    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(mnamejTextField1.getText().equals(""))
            errjLabel5.setText("Enter medicine Name");
        else
            if(cnamejTextField5.getText().equals(""))
            errjLabel5.setText("Enter company Name");
        else
                if(dijTextField2.getText().equals(""))
            errjLabel5.setText("Enter Date of issue ");
        else
                    if(dejTextField3.getText().equals(""))
            errjLabel5.setText("Enter  Expiray Date");
        else
                        if(qtyjTextField4.getText().equals(""))
            errjLabel5.setText("Enter Medicine quantity");
        else
                        {
                            cobj.connect();
                            try
                            {
                              pst=cobj.con.prepareCall("{call prcinsertmedicine(?,?,?,?,?)}");
                              pst.setString(1, mnamejTextField1.getText().toUpperCase());
                              pst.setString(2, cnamejTextField5.getText().toUpperCase());
                              pst.setString(3, dijTextField2.getText());
                              pst.setString(4, dejTextField3.getText());
                              pst.setString(5, qtyjTextField4.getText());
                              if(pst.execute()==false)
                              {
                                  if(pst.getUpdateCount()>0)
                                      errjLabel5.setText("record save");
                                      mnamejTextField1.setText("");
                                      cnamejTextField5.setText("");
                                      dijTextField2.setText("");
                                      dejTextField3.setText("");
                                      qtyjTextField4.setText("");
                              }
                            }
                            catch(Exception ex)
                            {
                                System.out.println("error in save"+ex);   
                            }
                            cobj.disconnect();
                        }
    }//GEN-LAST:event_jButton1ActionPerformed

