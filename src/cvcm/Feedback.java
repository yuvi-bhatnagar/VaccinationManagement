    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
           cobj.connect();
           
           
                    try
                    {
                      pst=cobj.con.prepareStatement("select * from tbFeedback where CitizenId=? ");
                      pst.setString(1, jTextField1.getText());
                      rst=pst.executeQuery();
                      if(rst.next())
                       {
                              errjLabel6.setText("record found");
                              fid=rst.getLong(1);
                              jTextField1.setText(rst.getString(2));
                              dofjTextField1.setText(rst.getString(3));
                              adjTextField2.setText(rst.getString(4));
                              rejTextField3.setText(rst.getString(5));
                              sjTextField4.setText(rst.getString(6));
                              sjTextField4.setEditable(true);
                          }
                    }
                    catch(Exception ex)
                    {
                        System.out.println("error in search"+ex);
               }
                    cobj.disconnect();
    }//GEN-LAST:event_jButton3ActionPerformed

