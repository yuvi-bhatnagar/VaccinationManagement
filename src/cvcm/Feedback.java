    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(dofjTextField1.getText().equals(""))
            errjLabel6.setText("Enter Date of Feedbcak");
        else
            if(adjTextField2.getText().equals(""))
                errjLabel6.setText("enter action details");
        else
                if(rejTextField3.getText().equals(""))
                    errjLabel6.setText("enter remarks");
        else
                    if(jTextField1.getText().equals(""))
                        errjLabel6.setText("Enter AAdhar Card Number");
                    else 
                {
                        
                    cobj.connect();
                    try
                    {
                      pst=cobj.con.prepareCall("{call prcinsertfeedback(?,?,?,?,?)}");
                      pst.setString(1, jTextField1.getText());
                      pst.setString(2, dofjTextField1.getText());
                      pst.setString(3,adjTextField2.getText() );
                      pst.setString(4, rejTextField3.getText());
                      pst.setString(5, sjTextField4.getText());
                      if(pst.execute()==false)
                          if(pst.getUpdateCount()>0)
                          {
                              errjLabel6.setText("record save");
                              dofjTextField1.setText("");
                              adjTextField2.setText("");
                              rejTextField3.setText("");
                              sjTextField4.setText("");
                              jTextField1.setText("");
                              
                          }
                    }
                    catch(Exception ex)
                    {
                        System.out.println("error in save"+ex);
               }
                    cobj.disconnect();
                }
    }//GEN-LAST:event_jButton2ActionPerformed

